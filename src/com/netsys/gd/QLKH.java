    private void filltable() {
        DefaultTableModel model=(DefaultTableModel) tblkh.getModel();
        model.setRowCount(0);
        try {
            List<khachhang> list=dao.selectAll();
            for(khachhang kh:list){
                Object[] row={kh.getMakh(),kh.getTenkh(),kh.getTienconlai(),kh.getSdt()};
                System.out.println(kh.getSdt());
                model.addRow(row);
            }
        } catch (Exception e) {
            TBBOX.alert(this,"Lỗi Truy Vấn");
        }
    }
    private void display(int i){
        khachhang kh=Listkh.get(i);
        txtmakh.setText(kh.getMakh());
        txttenkh.setText(kh.getTenkh());
        txttienconlai.setText(kh.getTienconlai().toString());
        txtsdt.setText(kh.getSdt());
    }
    private boolean ktList(){
        if(Listkh.size()<1){
            return true;
        }
        else{
            return false;
        }
    }
    private boolean kttt(){
        if(txtmakh.getText().equals("")||txtmakh.getText()==null){
            TBBOX.alert(this,"thông tin không được trống");
            txtmakh.requestFocus();
            return false;
        }
        else if(txttenkh.getText().equals("") || txttenkh.getText()==null){
            TBBOX.alert(this,"thông tin không được trống");
            txttenkh.requestFocus();
            return false;
        }
        else if(txttienconlai.getText().equals("") || txttienconlai.getText()==null){
            TBBOX.alert(this,"thông tin không được để trống");
            txttienconlai.requestFocus();
            return false;
        }
        return true;
    }
    private void them(){
        khachhang kh=new khachhang();
        kh.setMakh(txtmakh.getText());
        kh.setTenkh(txttenkh.getText());
        kh.setTienconlai(Double.parseDouble(txttienconlai.getText()));
        kh.setSdt(txtsdt.getText());
        try {
            khachhang a=dao.selectByid(txtmakh.getText());
            if(a!=null){
                TBBOX.alert(this, "Khách Hàng Đã Tồn Tại Trong Hệ Thống Vô lòng Cập Nhật Hoặc Nhập Lại TT");
            }
            else{
                dao.insert(kh);
                TBBOX.alert(this,"thêm Thành Công");   
                clear();
                filltable();
                Listkh=dao.selectAll();
            }
        } catch (Exception e) {
            TBBOX.alert(this,"Đã Xãy Ra Lỗi");
            System.out.println(e);
        }
    }

    private void clear() {
        txtmakh.setText("");
        txttenkh.setText("");
        txtsdt.setText("");
        txttienconlai.setText("");
    }
    private void sua(){
          khachhang kh=new khachhang();
        kh.setMakh(txtmakh.getText());
        kh.setTenkh(txttenkh.getText());
        kh.setTienconlai(Double.parseDouble(txttienconlai.getText()));
        kh.setSdt(txtsdt.getText());
        try {
            khachhang a=dao.selectByid(txtmakh.getText());
            if(a!=null){
                 dao.update(kh);
                TBBOX.alert(this,"Cập Nhật Thành Công");   
                clear();
                filltable();
                Listkh=dao.selectAll();
              
            }
            else{
                 TBBOX.alert(this, "Khách Hàng Không Tồn Tại Trong Hệ Thống Vô lòng Thêm Hoặc Nhập Lại TT");
            }
        } catch (Exception e) {
            TBBOX.alert(this,"Đã Xãy Ra Lỗi");
            System.out.println(e);
        }
    }
    private void xoa(){
        try {
            khachhang a=dao.selectByid(txtmakh.getText());
            if(a!=null){
                dao.delete(a.getMakh());
                TBBOX.alert(this,"Xóa Thành Công");
                clear();
                filltable();
                Listkh=dao.selectAll();
            }
            else{
                TBBOX.alert(this,"Khách Hàng Không Tồn Tại");
            }
        } catch (Exception e) {
            TBBOX.alert(this, "đã Xãy Ra Lỗi");
            System.out.println(e);
        }
    }
